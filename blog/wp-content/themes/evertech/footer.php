<script>
var everbotHistory = [];
var everbotOpen   = false;
var everbotApiUrl = '<?php echo esc_url(rtrim(dirname(get_option("home")), "/") . "/chatbot-api.php"); ?>';

function everbotToggle() {
    everbotOpen = !everbotOpen;
    document.getElementById('everbot-window').style.display = everbotOpen ? 'flex' : 'none';
    if (everbotOpen) document.getElementById('everbot-input').focus();
}

function everbotKeyPress(e) { if (e.key === 'Enter') everbotSend(); }

function everbotSend() {
    var input = document.getElementById('everbot-input');
    var msg   = input.value.trim();
    if (!msg) return;
    everbotAddMessage(msg, 'user');
    input.value = '';
    var historySnapshot = everbotHistory.slice(-10);
    everbotHistory.push({role: 'user', content: msg});
    var typing = everbotAddTyping();
    fetch(everbotApiUrl, {
        method: 'POST',
        headers: {'Content-Type': 'application/json'},
        body: JSON.stringify({message: msg, history: historySnapshot})
    })
    .then(function(r) { return r.json(); })
    .then(function(data) {
        typing.remove();
        var reply = data.reply || 'Sorry, I could not process your request.';
        everbotAddMessage(reply, 'bot');
        everbotHistory.push({role: 'assistant', content: reply});
    })
    .catch(function() {
        typing.remove();
        everbotAddMessage('Sorry, something went wrong. Please try again.', 'bot');
    });
}

function everbotAddMessage(text, role) {
    var msgs = document.getElementById('everbot-messages');
    var div  = document.createElement('div');
    div.className = 'everbot-msg ' + role;
    div.innerHTML = '<div class="everbot-bubble">' + everbotEscape(text) + '</div>';
    msgs.appendChild(div);
    msgs.scrollTop = msgs.scrollHeight;
    return div;
}

function everbotAddTyping() {
    var msgs = document.getElementById('everbot-messages');
    var div  = document.createElement('div');
    div.className = 'everbot-msg bot everbot-typing';
    div.innerHTML = '<div class="everbot-bubble"><span class="everbot-dot"></span><span class="everbot-dot"></span><span class="everbot-dot"></span></div>';
    msgs.appendChild(div);
    msgs.scrollTop = msgs.scrollHeight;
    return div;
}

function everbotEscape(t) {
    return t.replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;').replace(/\n/g,'<br>');
}
</script>